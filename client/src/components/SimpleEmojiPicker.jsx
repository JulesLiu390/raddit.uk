export default function SimpleEmojiPicker({ onEmojiSelect, onClose }) {
  const [activeCategory, setActiveCategory] = useState(0);
  const [recentEmojis, setRecentEmojis] = useState([]);

  useEffect(() => {
    setRecentEmojis(getRecentEmojis());
  }, []);

  const handleEmojiClick = (emoji, e) => {
    const updated = saveRecentEmoji(emoji);
    setRecentEmojis(updated);
    onEmojiSelect(emoji, e);
    onClose();
  };

  // Build categories with dynamic recent
  const categories = [
    {
      name: 'æœ€è¿‘',
      icon: 'ğŸ•',
      emojis: recentEmojis.length > 0 ? recentEmojis : ['ğŸ‘', 'â¤ï¸', 'ğŸ˜‚', 'ğŸ˜', 'ğŸ¤®', 'ğŸ˜¢', 'ğŸ˜®', 'ğŸ”¥', 'ğŸ‘', 'ğŸ‰', 'ğŸ’¯', 'ğŸ™']
    },
    ...EMOJI_CATEGORIES
  ];

  return (
    <div className="simple-emoji-picker">
      {/* Category Navigation */}
      <div className="sep-categories">
        {categories.map((cat, idx) => (
          <button
            key={cat.name}
            className={`sep-cat-btn ${activeCategory === idx ? 'active' : ''}`}
            onClick={() => setActiveCategory(idx)}
            title={cat.name}
          >
            {cat.icon}
          </button>
        ))}
      </div>

      {/* Emoji Grid */}
      <div className="sep-emoji-grid">
        {categories[activeCategory].emojis.map((emoji, idx) => (
          <button
            key={idx}
            className="sep-emoji-btn"
            onClick={(e) => handleEmojiClick(emoji, e)}
          >
            {emoji}
          </button>
        ))}
      </div>
    </div>
  );
}
